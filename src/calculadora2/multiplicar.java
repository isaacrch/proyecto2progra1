
package calculadora2;

public class multiplicar {
  
    float valor1,valor2,total;

    public multiplicar(float valor1, float valor2) {
        this.valor1 = valor1;
        this.valor2 = valor2;
    }
    
    public float multiplicacion () {
        total = valor1*valor2;
        
        return total;
        
        
    }
    
}
