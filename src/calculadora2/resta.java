
package calculadora2;

public class resta {
    
    float valor1,valor2,total;

    public resta(float valor1, float valor2) {
        this.valor1 = valor1;
        this.valor2 = valor2;
      
    }
    public float restar (){
        
        total=valor1-valor2;
        
        return total;
        
    }
    
}
